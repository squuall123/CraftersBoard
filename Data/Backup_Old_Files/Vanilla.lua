-- Create backward compatibility layer for existing code
local VanillaDataBackwardCompat = {}
VanillaDataBackwardCompat.SPELL_TO_RECIPE = {}

-- Flatten the profession-organized data into a single spell->recipe mapping for backward compatibility
for professionId, recipes in pairs(CraftersBoard.VanillaData) do
    if type(recipes) == "table" then
        for spellId, recipeName in pairs(recipes) do
            VanillaDataBackwardCompat.SPELL_TO_RECIPE[spellId] = recipeName
        end
    end
end

-- Export both structures for compatibility
CraftersBoard_VanillaData = VanillaDataBackwardCompat
CraftersBoard.VanillaData = CraftersBoard.VanillaData

-- Return for direct require() usage
return CraftersBoard.VanillaData
